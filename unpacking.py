#unpacking list
list = [2, 2, 4, 5, 6, 7]

u, v, w, x, y, z = list

print(u)
print(v)
print(w)
print(x)
print(y)
print(z)

#unpacking tuple
tuple = (2, 2, 4, 5, 6, 7)

u, v, w, x, y, z = tuple

print(u)
print(v)
print(w)
print(x)
print(y)
print(z)
